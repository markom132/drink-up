package com.party_up.network.model.enums;

/**
 * Enum representing the status of a User/Account.
 * The user can either be ACTIVE or INACTIVE.
 */
public enum AccountStatus {

    /**
     * Indicates that the user is currently active.
     */
    ACTIVE,

    /**
     * Indicates that user is currently inactive.
     */
    INACTIVE
}
